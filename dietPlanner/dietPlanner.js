document.getElementById("generatePlan").addEventListener("click", function () {
  const region = document.getElementById("region").value;
  const mealPlanDiv = document.getElementById("mealPlan");

  if (!region) {
    alert("Please select a cuisine.");
    return;
  }

  const weeklyPlan = weeklyRecipes[region];
  mealPlanDiv.innerHTML = `<h2>Your Weekly Meal Plan (${region
    .replace("_", " ")
    .toUpperCase()} Cuisine):</h2>`;

  weeklyPlan.forEach((dayPlan) => {
    const dayDiv = document.createElement("div");
    dayDiv.className = "day-plan";

    const dayTitle = document.createElement("h3");
    dayTitle.innerText = dayPlan.day;
    dayDiv.appendChild(dayTitle);

    Object.entries(dayPlan.meals).forEach(([meal, dish]) => {
      const mealDiv = document.createElement("div");
      mealDiv.className = "meal";
      mealDiv.innerText = `${
        meal.charAt(0).toUpperCase() + meal.slice(1)
      }: ${dish}`;

      const recipeDiv = document.createElement("div");
      recipeDiv.className = "recipe";
      recipeDiv.style.display = "none"; // Initially hide the recipe
      recipeDiv.innerHTML = recipes[dish] || "Recipe not found.";

      mealDiv.addEventListener("click", function () {
        recipeDiv.style.display =
          recipeDiv.style.display === "none" ? "block" : "none";
      });

      mealDiv.appendChild(recipeDiv);
      dayDiv.appendChild(mealDiv);
    });

    mealPlanDiv.appendChild(dayDiv);
  });
});
